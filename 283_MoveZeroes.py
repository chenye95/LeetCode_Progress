"""
Given an integer array nums, move all 0's to the end of it while maintaining the relative order of the non-zero elements

Note that you must do this in-place without making a copy of the array.
"""
from copy import deepcopy
from typing import List


def move_zeroes_all_swap(nums: List[int]) -> None:
    """
    :param nums: move all zeros to the end of list, while relative order of the non-zero elements is maintained,
        in place
    """
    last_none_zero_at = 0
    for i in range(len(nums)):
        if nums[i] != 0:
            if i > last_none_zero_at:
                nums[last_none_zero_at], nums[i] = nums[i], nums[last_none_zero_at]
            last_none_zero_at += 1


def move_zeroes_append_zeroes(nums: List[int]) -> None:
    """
    :param nums: move all zeros to the end of list, while relative order of the non-zero elements is maintained,
        in place
    """
    last_none_zero_at = 0
    for num_i in nums:
        if num_i != 0:
            nums[last_none_zero_at] = num_i
            last_none_zero_at += 1

    for i in range(last_none_zero_at, len(nums)):
        nums[i] = 0


