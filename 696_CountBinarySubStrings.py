"""
Give a string s, count the number of non-empty (contiguous) substrings that have the same number of 0's and 1's, and
 all the 0's and all the 1's in these substrings are grouped consecutively.

Substrings that occur multiple times are counted the number of times they occur.
"""


def count_binary_substrings_1(s: str) -> int:
    """
    :param s: binary string of 0s and 1s with 1 <= len(s) <= 50,000
    :return: count of non-empty contiguous substrings that equal to '1' * n + '0' * n or '0' * n + '1' * n
    """
    previous_count = current_count = 0
    current_c = ''

    return_count = 0
    for s_i in s:
        if s_i == current_c:
            current_count += 1
        else:
            previous_count = current_count
            current_count, current_c = 1, s_i
        if current_count <= previous_count:
            return_count += 1

    return return_count


def count_binary_substrings_2(s: str) -> int:
    """
    :param s: binary string of 0s and 1s with 1 <= len(s) <= 50,000
    :return: count of non-empty contiguous substrings that equal to '1' * n + '0' * n or '0' * n + '1' * n
    """
    previous_count = current_count = 0
    current_c = ''

    return_count = 0
    for s_i in s:
        if s_i == current_c:
            current_count += 1
        else:
            return_count += min(previous_count, current_count)
            previous_count = current_count
            current_count, current_c = 1, s_i

    return_count += min(previous_count, current_count)

    return return_count


