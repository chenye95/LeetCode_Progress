"""
There are n cities. Some of them are connected, while some are not. If city a is connected directly with city b, and
 city b is connected directly with city c, then city a is connected indirectly with city c.

A province is a group of directly or indirectly connected cities and no other cities outside of the group.

You are given an n x n matrix isConnected where isConnected[i][j] = 1 if the ith city and the jth city are directly
 connected, and isConnected[i][j] = 0 otherwise.

Return the total number of provinces.
"""
from typing import List

from _Union_Find import UnionFindArray


def count_province_bfs(is_connected: List[List[int]]) -> int:
    """
    BFS Algorithm

    :param is_connected: connection matrix of 0 and 1 such that is_connected[i][j] = 1 if i and j are directly connected
    :return: number of connected components
    """
    connected_components = 0
    not_visited = [True] * len(is_connected)

    for i in range(len(is_connected)):
        if not_visited[i]:
            connected_components += 1

            next_visit = [i]
            not_visited[i] = False

            while next_visit:
                for j, c_ij in enumerate(is_connected[next_visit.pop(0)]):
                    if c_ij and not_visited[j]:
                        not_visited[j] = False
                        next_visit.append(j)

    return connected_components


def count_province_union_find(is_connected: List[List[int]]) -> int:
    """
    :param is_connected: connection matrix of 0 and 1 such that is_connected[i][j] = 1 if i and j are directly connected
    :return: number of connected components
    """
    union_find_object = UnionFindArray(len(is_connected))
    for i, connected_i in enumerate(is_connected):
        for j, c_ij in enumerate(connected_i[i + 1:], start=i + 1):
            if c_ij:
                union_find_object.unify(i, j)
    return union_find_object.components_count()


