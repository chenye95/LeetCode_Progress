"""
Given an array of integers nums sorted in ascending order, find the starting and ending position of a given target value

If target is not found in the array, return [-1, -1].
"""
from bisect import bisect_left, bisect_right
from typing import List, Tuple


def search_range_single_pass(nums: List[int], target: int) -> Tuple[int, int]:
    """
    :param nums: integers in non-descending order, with repetitive values, 0 <= len(nums) <= 10^5
    :param target: -10^9 <= target <= 10^9
    :return: (First_Occurrence, Last_Occurrence) of target in nums, if target in nums, else (-1, -1)
    """
    start, end = 0, len(nums) - 1
    while start <= end:
        mid = (start + end) // 2
        if nums[start] == nums[end] == target:
            return start, end

        if nums[mid] < target:
            start = mid + 1
        elif nums[mid] > target:
            end = mid - 1
        else:  # nums[mid] == target
            if nums[start] < target:
                start += 1
            if nums[end] > target:
                end -= 1
    return -1, -1


def search_range_two_pass(nums: List[int], target: int) -> Tuple[int, int]:
    """
    :param nums: integers in non-descending order, with repetitive values, 0 <= len(nums) <= 10^5
    :param target: -10^9 <= target <= 10^9
    :return: (First_Occurrence, Last_Occurrence) of target in nums, if target in nums, else (-1, -1)
    """
    if not nums:
        return -1, -1

    if nums[0] == target:
        left_bound_minus_one = -1
    else:
        low, high = 0, len(nums) - 1
        while low < high:
            middle = (low + high) // 2
            if nums[middle] < target:
                low = middle + 1
            elif nums[middle] >= target:
                high = middle - 1
        left_bound_minus_one = low - 1 if nums[low] == target else low

    if nums[-1] == target:
        right_bound_plus_one = len(nums)
    else:
        low, high = 0, len(nums) - 1
        while low < high:
            middle = (low + high) // 2
            if nums[middle] <= target:
                low = middle + 1
            elif nums[middle] > target:
                high = middle - 1
        right_bound_plus_one = low + 1 if nums[low] == target else low

    if left_bound_minus_one < right_bound_plus_one:
        return left_bound_minus_one + 1, right_bound_plus_one - 1
    else:
        return -1, -1


def search_range_bisect(nums: List[int], target: int) -> Tuple[int, int]:
    """
    :param nums: integers in non-descending order, with repetitive values, 0 <= len(nums) <= 10^5
    :param target: -10^9 <= target <= 10^9
    :return: (First_Occurrence, Last_Occurrence) of target in nums, if target in nums, else (-1, -1)
    """
    if not nums:
        return -1, -1

    lower_bound, upper_bound = bisect_left(nums, target), bisect_right(nums, target)
    if 0 <= lower_bound <= min(upper_bound, len(nums) - 1) and nums[lower_bound] == target:
        return lower_bound, upper_bound - 1
    else:
        return -1, -1


