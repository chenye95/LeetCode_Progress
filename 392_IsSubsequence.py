"""
Given two strings s and t, return true if s is a subsequence of t, or false otherwise.

A subsequence of a string is a new string that is formed from the original string by deleting some (can be none) of the
 characters without disturbing the relative positions of the remaining characters. (i.e., "ace" is a subsequence of
 "abcde" while "aec" is not).
"""


def is_subsequence(s: str, t: str) -> bool:
    """
    :param s: 1 <= len(s) <= 100, consists of lower case english letters only
    :param t: 1 <= len(s) <= 10000, consists of lower case english letters only
    :return: whether s is a subsequence of t
    """
    if not s:
        return True
    i = 0
    for t_j in t:
        if t_j == s[i]:
            i += 1
            if i == len(s):
                return True
    return False


