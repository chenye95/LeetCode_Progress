"""
You are given a string s consisting only of the characters '0' and '1'. In one operation, you can change any '0' to '1'
 or vice versa.

The string is called alternating if no two adjacent characters are equal. For example, the string "010" is alternating,
 while the string "0100" is not.

Return the minimum number of operations needed to make s alternating.
"""


def min_operations(s: str) -> int:
    """
    :param s: string of '0' and '1' where 1 <= len(s) <= 10000
    :return: min change operations to convert the strings to alternating binary string
    """
    # note there are only two choices for the alternating binary string
    # '010101....' or '101010...'
    # to check against '010101...' compare c to i % 2
    first_option = sum((i % 2 == 0) is (c == '0') for i, c in enumerate(s))
    # to check against '101010...', calculate len(s) - first_option
    return min(first_option, len(s) - first_option)


