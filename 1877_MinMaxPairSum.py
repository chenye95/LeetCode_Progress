"""
The pair sum of a pair (a,b) is equal to a + b. The maximum pair sum is the largest pair sum in a list of pairs.
- For example, if we have pairs (1,5), (2,3), and (4,4), the maximum pair sum would be max(1 + 5, 2 + 3, 4 + 4) =
    max(6, 5, 8) = 8.

Given an array nums of even length n, pair up the elements of nums into n / 2 pairs such that:
- Each element of nums is in exactly one pair, and
- The maximum pair sum is minimized.

Return the minimized maximum pair sum after optimally pairing up the elements.
"""
from typing import List


def min_pair_sum(nums: List[int]) -> int:
    """
    :param nums: 2 <= len(nums) <= 10**5, and len(nums) is even, 1 <= nums[i] <= 10**5
    :return: minimized maximum pair sum after optimally pairing up the elements
    """
    n = len(nums) // 2
    nums.sort()
    return max(a + b for a, b in zip(nums[:n], nums[len(nums) - 1: n - 1: -1]))


