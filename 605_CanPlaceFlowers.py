"""
You have a long flowerbed in which some of the plots are planted, and some are not. However, flowers cannot be planted
 in adjacent plots.

Given an integer array flowerbed containing 0's and 1's, where 0 means empty and 1 means not empty, and an integer n,
 return if n new flowers can be planted in the flowerbed without violating the no-adjacent-flowers rule.
"""
from typing import List


def can_place_flowers(flower_bed: List[bool], n: int) -> bool:
    """
    :param flower_bed: 1 <= len(flower_bed) <= 2e4, of 0 and 1, guaranteed there are no adjacent 1s
    :param n: 0 <= n <= len(flower_bed) number of flowers need to be planted
    :return: whether we can plant all n flowers
    """
    if n == 0:
        return True

    placed_count = 0
    for i in range(len(flower_bed)):
        if not flower_bed[i] and (i == 0 or not flower_bed[i - 1]) and \
                (i == len(flower_bed) - 1 or not flower_bed[i + 1]):
            flower_bed[i] = True
            placed_count += 1
            if placed_count >= n:
                return True

    return False


