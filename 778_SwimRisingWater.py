"""
On an N x N grid, each square grid[i][j] represents the elevation at that point (i,j).

Now rain starts to fall. At time t, the depth of the water everywhere is t. You can swim from a square to another
 4-directionally adjacent square if and only if the elevation of both squares individually are at most t. You can swim
 infinite distance in zero time. Of course, you must stay within the boundaries of the grid during your swim.

You start at the top left square (0, 0). What is the least time until you can reach the bottom right square (N-1, N-1)?
"""
from heapq import heappush, heappop
from typing import List


def swim_in_water_set(grid: List[List[int]]) -> int:
    """
    :param grid: 2 <= n <= 50, n == len(grid) == len(grid[0]), and grid[i][j] is permutation of [0, ..., n * n - 1]
    :return: min time to swim from (0, 0) to (n - 1, n - 1)
    """
    n = len(grid)
    sort_by_time = [(grid[0][0], 0, 0)]
    seen = {(0, 0)}
    max_t = 0
    x = y = 0

    while x < n - 1 or y < n - 1:
        t, x, y = heappop(sort_by_time)
        max_t = max(max_t, t)
        for next_x, next_y in [(x + 1, y), (x - 1, y), (x, y + 1), (x, y - 1)]:
            if 0 <= next_x < n and 0 <= next_y < n and (next_x, next_y) not in seen:
                seen.add((next_x, next_y))
                heappush(sort_by_time, (grid[next_x][next_y], next_x, next_y))

    return max_t


def swim_in_water_bool(grid: List[List[int]]) -> int:
    """
    :param grid: 2 <= n <= 50, n == len(grid) == len(grid[0]), and grid[i][j] is permutation of [0, ..., n * n - 1]
    :return: min time to swim from (0, 0) to (n - 1, n - 1)
    """
    n = len(grid)
    sort_by_time = [(grid[0][0], 0, 0)]
    not_seen = [[True] * n for _ in range(n)]
    not_seen[0][0] = False
    max_t = 0
    x = y = 0

    while x < n - 1 or y < n - 1:
        t, x, y = heappop(sort_by_time)
        max_t = max(max_t, t)
        for next_x, next_y in [(x + 1, y), (x - 1, y), (x, y + 1), (x, y - 1)]:
            if 0 <= next_x < n and 0 <= next_y < n and not_seen[next_x][next_y]:
                not_seen[next_x][next_y] = False
                heappush(sort_by_time, (grid[next_x][next_y], next_x, next_y))

    return max_t


