from typing import List, Union


class NestedInteger:
    def __init__(self, val: Union[int, List['NestedInteger']], object_is_integer: bool):
        self.val = val
        self._is_integer = object_is_integer

    def is_integer(self) -> bool:
        return self._is_integer

    def get_integer(self) -> int:
        return self.val if self._is_integer else None

    def get_list(self) -> List['NestedInteger']:
        return self.val if not self._is_integer else None

    @staticmethod
    def create_obj(val: Union[int, List]) -> 'NestedInteger':
        if isinstance(val, int):
            return NestedInteger(val, True)
        else:
            return NestedInteger([NestedInteger.create_obj(val_i) for val_i in val], False)


class NestedIterator:
    def __init__(self, nested_list: List[NestedInteger]):
        self.pointer = -1
        self.flattened_data = []
        self.flatten(nested_list)

    def flatten(self, nest_integer_list: List[NestedInteger]) -> None:
        for item_i in nest_integer_list:
            if item_i.is_integer():
                self.flattened_data.append(item_i.get_integer())
            else:
                self.flatten(item_i.get_list())

    def has_next(self) -> bool:
        """
        :return: whether there still exist some integers in the list
        """
        return self.pointer < len(self.flattened_data) - 1

    def next(self) -> int:
        """
        :return: next integer in the nested list
        """
        self.pointer += 1
        return self.flattened_data[self.pointer]


