"""
Given an array A of non-negative integers, half of the integers in A are odd, and half of the integers are even.

Sort the array so that whenever A[i] is odd, i is odd; and whenever A[i] is even, i is even.

You may return any answer array that satisfies this condition.
"""
from typing import List


def sort_array_by_parity(nums: List[int]) -> List[int]:
    """
    :param nums: array of non-negative integers, half of which are odd, and half even
    :return: a[i] is odd when i is odd; a[i] is even when i is even
    """
    odd_pointer = 1
    for even_pointer in range(0, len(nums), 2):
        if nums[even_pointer] % 2:
            # need switch to odd positions
            while nums[odd_pointer] % 2:
                odd_pointer += 2
            nums[odd_pointer], nums[even_pointer] = nums[even_pointer], nums[odd_pointer]
    return nums


