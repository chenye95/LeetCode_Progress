"""
In a given 2D binary array grid, there are two islands.  (An island is a 4-directionally connected group of 1s not
 connected to any other 1s.)

Now, we may change 0s to 1s so as to connect the two islands together to form 1 island.

Return the smallest number of 0s that must be flipped.  (It is guaranteed that the answer is at least 1.)
"""
from typing import List


def shortest_bridge(grid: List[List[int]]) -> int:
    """
    :param grid: two islands exist in the 2D binary grid. Land is marked as 1, water as 0
    :return: shortest bridge (change 0 to 1) to connect the two islands
    """

    def mark_island(x: int, y: int):
        for move_x, move_y in move_directions:
            if 0 <= x + move_x < m and 0 <= y + move_y < n and grid[x + move_x][y + move_y] == 1:
                explore_steps.append((x + move_x, y + move_y))
                grid[x + move_x][y + move_y] = 2
                mark_island(x + move_x, y + move_y)

    m, n = len(grid), len(grid[0])
    move_directions = [(-1, 0), (1, 0), (0, -1), (0, 1)]

    # Find the first island - Depth First Search
    found_one_island = False
    explore_steps = []
    for i in range(m):
        for j in range(n):
            if grid[i][j]:
                explore_steps.append((i, j))
                grid[i][j] = 2
                found_one_island = True
                mark_island(i, j)
                break
        if found_one_island:
            break

    # Connect shortest bridge - Breadth First Search
    current_step = 0
    while explore_steps:
        next_explore = []
        while explore_steps:
            current_x, current_y = explore_steps.pop()
            for d_x, d_y in move_directions:
                if 0 <= current_x + d_x < m and 0 <= current_y + d_y < n and \
                        grid[current_x + d_x][current_y + d_y] != 2:
                    if grid[current_x + d_x][current_y + d_y] == 1:
                        # Found the second island
                        return current_step
                    # grid[current_x + d_x][current_y + d_y] is water and equals to 0
                    grid[current_x + d_x][current_y + d_y] = 2
                    next_explore.append((current_x + d_x, current_y + d_y))
        current_step += 1
        explore_steps = next_explore

    return -1


