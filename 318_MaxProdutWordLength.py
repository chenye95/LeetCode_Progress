"""
Given a string array words, return the maximum value of length(word[i]) * length(word[j]) where the two words do not
 share common letters. If no such two words exist, return 0.
"""
from typing import List


def max_product_word_length_frozen_set(words: List[str]) -> int:
    """
    :param words: 2 <= len(words) <= 1000, and 1 <= len(words[i]) <= 1000, contains only lowercase english letters
    :return: max(len(words[i]) * len(words[i])) where words[i] and words[j] do not share common letters
    """
    word_mask_len = {}
    for word_i in words:
        word_mask = frozenset(word_i)
        word_mask_len[word_mask] = max(word_mask_len.get(word_mask, 0), len(word_i))

    return max([word_mask_len[mask_a] * word_mask_len[mask_b] for mask_a in word_mask_len for mask_b in word_mask_len
                if not mask_a & mask_b] + [0])


def max_product_word_length_binary(words: List[str]) -> int:
    """
    :param words: 2 <= len(words) <= 1000, and 1 <= len(words[i]) <= 1000, contains only lowercase english letters
    :return: max(len(words[i]) * len(words[i])) where words[i] and words[j] do not share common letters
    """
    word_mask_len = {}
    ord_a = ord('a')
    for word_i in words:
        word_mask = 0
        for c in word_i:
            word_mask |= (1 << (ord(c) - ord_a))
        word_mask_len[word_mask] = max(word_mask_len.get(word_mask, 0), len(word_i))

    return max([word_mask_len[mask_a] * word_mask_len[mask_b] for mask_a in word_mask_len for mask_b in word_mask_len
                if not mask_a & mask_b] + [0])


