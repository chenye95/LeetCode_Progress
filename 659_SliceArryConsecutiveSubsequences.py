"""
You are given an integer array nums that is sorted in non-decreasing order.

Determine if it is possible to split nums into one or more subsequences such that both of the following conditions are
 true:
- Each subsequence is a consecutive increasing sequence (i.e. each integer is exactly one more than the previous
    integer).
- All subsequences have a length of 3 or more.

Return true if you can split nums according to the above conditions, or false otherwise.

A subsequence of an array is a new array that is formed from the original array by deleting some (can be none) of the
 elements without disturbing the relative positions of the remaining elements. (i.e., [1,3,5] is a subsequence of
 [1,2,3,4,5] while [1,3,2] is not).
"""
from heapq import heappop, heappush
from typing import List


def is_possible_list(nums: List[int]) -> bool:
    """
    :param nums: 1 <= len(nums) <= 10000 in non decreasing order, and -1000 <= nums[i] <= 1000
    :return: whether nums can be split into several consecutive subsequences
    """
    end_at_num_minus_1 = []  # lens of subsequences end, in descending order
    end_at_num = []  # lens of subsequences end, in ascending order
    previous_num = -2000

    for current_num in nums:
        if current_num > previous_num + 1:
            # start a new sequence
            if (end_at_num and end_at_num[0] < 3) or (end_at_num_minus_1 and end_at_num_minus_1[-1] < 3):
                return False
            end_at_num_minus_1 = []
            end_at_num = [1]
        else:
            if current_num == previous_num + 1:
                if end_at_num_minus_1 and end_at_num_minus_1[-1] < 3:
                    return False
                end_at_num_minus_1 = end_at_num
                end_at_num_minus_1.reverse()
                end_at_num = []
            if end_at_num_minus_1:
                end_at_num.append(end_at_num_minus_1.pop() + 1)
            else:
                end_at_num.insert(0, 1)
        previous_num = current_num

    if (end_at_num and end_at_num[0] < 3) or (end_at_num_minus_1 and end_at_num_minus_1[-1] < 3):
        return False
    else:
        return True


def is_possible_heap(nums: List[int]) -> bool:
    """
    :param nums: 1 <= len(nums) <= 10000 in non decreasing order, and -1000 <= nums[i] <= 1000
    :return: whether nums can be split into several consecutive subsequences
    """
    subsequence_len_heap = [(nums[0], 1), ]
    # (largest element so far in the subsequence, subsequence len)

    i = 1
    while i < len(nums):
        num = nums[i]
        if not subsequence_len_heap or subsequence_len_heap[0][0] == num:
            heappush(subsequence_len_heap, (num, 1))
            i += 1
        elif subsequence_len_heap[0][0] + 1 == num:
            _, subsequence_len = heappop(subsequence_len_heap)
            heappush(subsequence_len_heap, (num, subsequence_len + 1))
            i += 1
        else:
            _, subsequence_len = heappop(subsequence_len_heap)
            if subsequence_len < 3:
                return False

    return not any(subsequence_len < 3 for _, subsequence_len in subsequence_len_heap)


